    {
        if (dict.TryGetValue(key, out var value))
        {
            if (value is JsonElement element && element.ValueKind == JsonValueKind.Number)
            {
                return element.GetDouble();
            }
            if (double.TryParse(value?.ToString(), out var doubleValue))
            {
                return doubleValue;
            }
        }
        return defaultValue;
    }

